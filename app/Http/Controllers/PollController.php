    public function vote(Request $request, $q_id)
    {
        $this->validate($request, array(
            'choice' => 'required'
        ));

        $selected_option = $request->input('choice');
        
        $option = Option::where([
                ['id', '=', $selected_option],
                ['question_id', '=', $q_id],
            ])->first();

        $option->votes_count += 1;
        $option->save();

        Session::flash('success', 'Poll successfully registered');

        return redirect()->route('poll.show', $q_id);
    }

